#!/usr/bin/env python3
"""Generate Node-RED flows.json for the Robot Twin dashboard.

Run from this directory:  python gen_flows.py > ../flows.json

Outputs a Dashboard-2.0 layout:
  - Page "Live"
      - Group "Joints":     6 gauges (axis position, deg)
      - Group "Temps":      1 chart with 6 motor-temp series + threshold line
      - Group "Status":     ProgramState pill (text)
      - Group "Cycle":      cycle counter (big number)
  - Page "Recommendations"
      - JSON viewer for ActiveRecommendation
      - Approve / Reject buttons (Approve calls ApproveRecommendation method)

Connection: a single OpcUa-Client config + an OpcUa-Endpoint config,
plus one read-poll inject (250 ms) per axis position and one for each motor
temperature. We poll instead of subscribe to avoid asyncua server-side
subscription weirdness with multiple Node-RED reconnects during dev.
"""
from __future__ import annotations

import json
import sys
import uuid


def _id() -> str:
    return uuid.uuid4().hex[:16]


# ────────── stable IDs (referenced from multiple nodes) ──────────
TAB = "tab.live"
DASH = "ui.base"
PAGE_LIVE = "ui.page.live"
PAGE_RECO = "ui.page.reco"
GRP_JOINTS = "ui.grp.joints"
GRP_TEMPS = "ui.grp.temps"
GRP_STATUS = "ui.grp.status"
GRP_CYCLE = "ui.grp.cycle"
GRP_RECO = "ui.grp.reco"
GRP_APPROVE = "ui.grp.approve"

OPCUA_ENDPOINT = "opcua.endpoint"

NS = "ns=2"
NS_RECO = "ns=3"

# Each axis: ActualPosition (Double), MotorTemperature (Double)
# OPC UA NodeIds in asyncua follow the pattern ns=N;s=Path/To/Node — but
# asyncua returns *numeric* identifiers when nodes are auto-allocated.
# Easier to use the BrowsePath syntax, which node-red-contrib-opcua supports.
def axis_pos_nodeid(axis: int) -> str:
    return f"{NS};s=RobotController.MotionDevice.Axis{axis}.ActualPosition"


def axis_motor_nodeid(axis: int) -> str:
    return f"{NS};s=RobotController.MotionDevice.Axis{axis}.MotorTemperature"


PROGSTATE_NID = f"{NS};s=RobotController.ProgramState"
CYCLE_NID = f"{NS};s=RobotController.CycleCounter"
ACTIVE_RECO_NID = f"{NS_RECO};s=RobotRecommendations.ActiveRecommendation"


def main() -> None:
    nodes: list[dict] = []

    # ─── Tab ───
    nodes.append({
        "id": TAB,
        "type": "tab",
        "label": "Robot Twin",
        "disabled": False,
        "info": "Dashboard wiring; do not edit manually — regenerated by gen_flows.py",
    })

    # ─── OPC UA endpoint + client config ───
    nodes.append({
        "id": OPCUA_ENDPOINT,
        "type": "OpcUa-Endpoint",
        "endpoint": "opc.tcp://opcua-server:4840/axel/robot",
        "secpol": "None",
        "secmode": "None",
        "none": True,
        "login": False,
        "usercert": False,
        "usercertificate": "",
        "userprivatekey": "",
    })

    # ─── Dashboard 2.0 config ───
    nodes.append({
        "id": DASH,
        "type": "ui-base",
        "name": "Robot Twin",
        "path": "/nodered/ui",
        "appIcon": "",
        "showPathInSidebar": False,
        "showPageTitle": True,
        "navigationStyle": "default",
        "titleBarStyle": "default",
    })

    # Pages
    nodes.append({
        "id": PAGE_LIVE,
        "type": "ui-page",
        "name": "Live",
        "ui": DASH,
        "path": "/live",
        "icon": "speedometer",
        "layout": "grid",
        "theme": "ui.theme",
        "order": 1,
        "className": "",
        "visible": True,
        "disabled": False,
    })
    nodes.append({
        "id": PAGE_RECO,
        "type": "ui-page",
        "name": "Recommendations",
        "ui": DASH,
        "path": "/recommendations",
        "icon": "checkbox-marked-circle-outline",
        "layout": "grid",
        "theme": "ui.theme",
        "order": 2,
        "className": "",
        "visible": True,
        "disabled": False,
    })

    nodes.append({
        "id": "ui.theme",
        "type": "ui-theme",
        "name": "Default Theme",
        "colors": {
            "surface": "#ffffff",
            "primary": "#0094ce",
            "bgPage": "#eeeeee",
            "groupBg": "#ffffff",
            "groupOutline": "#cccccc",
        },
        "sizes": {
            "pagePadding": "12px",
            "groupGap": "12px",
            "groupBorderRadius": "8px",
            "widgetGap": "8px",
        },
    })

    # Groups
    for gid, name, page, w in [
        (GRP_JOINTS, "Joints (deg)", PAGE_LIVE, 12),
        (GRP_TEMPS, "Motor Temperatures (°C)", PAGE_LIVE, 12),
        (GRP_STATUS, "Status", PAGE_LIVE, 6),
        (GRP_CYCLE, "Cycles", PAGE_LIVE, 6),
        (GRP_RECO, "Active Recommendation", PAGE_RECO, 12),
        (GRP_APPROVE, "Operator Approval", PAGE_RECO, 12),
    ]:
        nodes.append({
            "id": gid,
            "type": "ui-group",
            "name": name,
            "page": page,
            "width": w,
            "height": 1,
            "order": 1,
            "showTitle": True,
            "className": "",
            "visible": True,
            "disabled": False,
        })

    # ─── Helper: 250 ms inject + OPC UA Item + OPC UA Client read ───
    def make_read(label: str, node_id: str, x: int, y: int, datatype: str = "Double"):
        inj = _id(); item = _id(); cli = _id()
        nodes.append({
            "id": inj,
            "type": "inject",
            "z": TAB,
            "name": f"poll {label}",
            "props": [{"p": "payload"}, {"p": "topic", "vt": "str"}],
            "repeat": "0.25",
            "crontab": "",
            "once": True,
            "onceDelay": "1.0",
            "topic": label,
            "payload": "",
            "payloadType": "date",
            "x": x,
            "y": y,
            "wires": [[item]],
        })
        nodes.append({
            "id": item,
            "type": "OpcUa-Item",
            "z": TAB,
            "name": label,
            "item": node_id,
            "datatype": datatype,
            "value": "",
            "x": x + 200,
            "y": y,
            "wires": [[cli]],
        })
        nodes.append({
            "id": cli,
            "type": "OpcUa-Client",
            "z": TAB,
            "endpoint": OPCUA_ENDPOINT,
            "action": "read",
            "deadbandtype": "a",
            "deadbandvalue": 1,
            "time": 10,
            "timeUnit": "s",
            "certificate": "n",
            "localfile": "",
            "localkeyfile": "",
            "securitymode": "None",
            "securitypolicy": "None",
            "name": "",
            "x": x + 380,
            "y": y,
            "wires": [[]],   # filled in by caller (return id)
        })
        return cli  # the node whose output to wire downstream

    # ─── Six axis-position polls → six gauges ───
    for i in range(1, 7):
        cli = make_read(f"axis{i}_pos", axis_pos_nodeid(i), 100, 100 + (i - 1) * 70, "Double")
        gauge = _id()
        nodes.append({
            "id": gauge,
            "type": "ui-gauge",
            "z": TAB,
            "group": GRP_JOINTS,
            "name": f"axis{i}",
            "label": f"Axis {i}",
            "order": i,
            "width": 4 if i <= 3 else 4,
            "height": 4,
            "gtype": "gauge",
            "min": -180,
            "max": 180,
            "units": "°",
            "icon": "",
            "iconColor": "#000000",
            "iconSize": 24,
            "segments": [
                {"from": "-180", "color": "#0094ce"},
                {"from": "180", "color": "#0094ce"},
            ],
            "className": "",
        })
        # wire the OPC UA client output to the gauge
        for n in nodes:
            if n["id"] == cli:
                n["wires"] = [[gauge]]

    # ─── Six motor-temp polls → ONE chart (separate series) ───
    motor_chart = _id()
    join_temp = _id()
    nodes.append({
        "id": join_temp,
        "type": "function",
        "z": TAB,
        "name": "label motor temp series",
        "func": (
            "// Pass-through that ensures msg.topic is the axis label so the"
            " chart renders one series per axis.\n"
            "msg.topic = msg.topic || 'unknown';\n"
            "msg.payload = Number(msg.payload);\n"
            "return msg;\n"
        ),
        "outputs": 1,
        "noerr": 0,
        "initialize": "",
        "finalize": "",
        "libs": [],
        "x": 700,
        "y": 600,
        "wires": [[motor_chart]],
    })
    for i in range(1, 7):
        cli = make_read(f"axis{i}_motorT", axis_motor_nodeid(i), 100, 600 + (i - 1) * 50, "Double")
        for n in nodes:
            if n["id"] == cli:
                n["wires"] = [[join_temp]]

    nodes.append({
        "id": motor_chart,
        "type": "ui-chart",
        "z": TAB,
        "group": GRP_TEMPS,
        "name": "motor temps",
        "label": "Motor Temperatures",
        "order": 1,
        "chartType": "line",
        "category": "topic",
        "categoryType": "msg",
        "xAxisProperty": "",
        "xAxisPropertyType": "timestamp",
        "yAxisProperty": "payload",
        "yMin": "20",
        "yMax": "100",
        "action": "append",
        "limit": 240,
        "duration": "300",
        "durationUnit": "seconds",
        "showLegend": True,
        "removeOlder": "5",
        "removeOlderUnit": "60",
        "removeOlderPoints": "",
        "colors": [
            "#0094ce", "#76b900", "#f5a623", "#ff5d5d", "#9b51e0", "#5ce1e6",
        ],
        "width": 12,
        "height": 8,
        "className": "",
    })

    # ─── ProgramState text + InjectAnomaly button + CycleCounter ───
    cli_ps = make_read("ProgramState", PROGSTATE_NID, 100, 1200, "Int32")
    map_ps = _id()
    text_ps = _id()
    nodes.append({
        "id": map_ps,
        "type": "function",
        "z": TAB,
        "name": "ProgramState → label",
        "func": (
            "const labels = {0:'Idle',1:'Starting',2:'Running',3:'Stopping',"
            "4:'Stopped',5:'Aborted',6:'MaintenanceRequired'};\n"
            "msg.payload = labels[msg.payload] || 'Unknown';\n"
            "return msg;"
        ),
        "outputs": 1,
        "x": 600,
        "y": 1200,
        "wires": [[text_ps]],
    })
    for n in nodes:
        if n["id"] == cli_ps:
            n["wires"] = [[map_ps]]
    nodes.append({
        "id": text_ps,
        "type": "ui-text",
        "z": TAB,
        "group": GRP_STATUS,
        "name": "ProgramState",
        "label": "Program State",
        "order": 1,
        "format": "{{msg.payload}}",
        "layout": "row-spread",
        "style": False,
        "font": "",
        "fontSize": 24,
        "color": "",
        "className": "",
        "width": 6,
        "height": 2,
    })

    cli_cc = make_read("CycleCounter", CYCLE_NID, 100, 1280, "UInt64")
    text_cc = _id()
    for n in nodes:
        if n["id"] == cli_cc:
            n["wires"] = [[text_cc]]
    nodes.append({
        "id": text_cc,
        "type": "ui-text",
        "z": TAB,
        "group": GRP_CYCLE,
        "name": "CycleCounter",
        "label": "Cycle",
        "order": 1,
        "format": "{{msg.payload}}",
        "layout": "row-spread",
        "fontSize": 36,
        "width": 6,
        "height": 2,
    })

    # ─── Recommendations page: read ActiveRecommendation, Approve / Reject buttons ───
    cli_reco = make_read("ActiveRecommendation", ACTIVE_RECO_NID, 100, 1400, "String")
    text_reco = _id()
    for n in nodes:
        if n["id"] == cli_reco:
            n["wires"] = [[text_reco]]
    nodes.append({
        "id": text_reco,
        "type": "ui-text",
        "z": TAB,
        "group": GRP_RECO,
        "name": "ActiveRecommendation",
        "label": "JSON",
        "order": 1,
        "format": "<pre>{{msg.payload}}</pre>",
        "layout": "row-spread",
        "fontSize": 14,
        "width": 12,
        "height": 8,
    })

    btn_approve = _id()
    btn_reject = _id()
    fn_approve = _id()
    fn_reject = _id()
    cli_approve = _id()
    cli_reject = _id()

    nodes.append({
        "id": btn_approve,
        "type": "ui-button",
        "z": TAB,
        "group": GRP_APPROVE,
        "name": "Approve",
        "label": "Approve",
        "order": 1,
        "color": "#ffffff",
        "bgcolor": "#76b900",
        "icon": "check",
        "payload": "approve",
        "payloadType": "str",
        "topic": "approve",
        "topicType": "str",
        "width": 4,
        "height": 2,
        "wires": [[fn_approve]],
    })
    nodes.append({
        "id": btn_reject,
        "type": "ui-button",
        "z": TAB,
        "group": GRP_APPROVE,
        "name": "Reject",
        "label": "Reject",
        "order": 2,
        "color": "#ffffff",
        "bgcolor": "#ff5d5d",
        "icon": "close",
        "payload": "reject",
        "payloadType": "str",
        "topic": "reject",
        "topicType": "str",
        "width": 4,
        "height": 2,
        "wires": [[fn_reject]],
    })

    nodes.append({
        "id": fn_approve,
        "type": "function",
        "z": TAB,
        "name": "build approve method args",
        "func": (
            "msg.methodId = '"
            f"{NS_RECO};s=RobotRecommendations.ApproveRecommendation';\n"
            "msg.objectId = '"
            f"{NS_RECO};s=RobotRecommendations';\n"
            "msg.inputArguments = [\n"
            "  { dataType: 'String', value: msg.recoId || 'current' },\n"
            "  { dataType: 'Boolean', value: true },\n"
            "];\n"
            "msg.action = 'method';\n"
            "return msg;"
        ),
        "outputs": 1,
        "x": 700,
        "y": 1500,
        "wires": [[cli_approve]],
    })
    nodes.append({
        "id": fn_reject,
        "type": "function",
        "z": TAB,
        "name": "build reject method args",
        "func": (
            "msg.methodId = '"
            f"{NS_RECO};s=RobotRecommendations.ApproveRecommendation';\n"
            "msg.objectId = '"
            f"{NS_RECO};s=RobotRecommendations';\n"
            "msg.inputArguments = [\n"
            "  { dataType: 'String', value: msg.recoId || 'current' },\n"
            "  { dataType: 'Boolean', value: false },\n"
            "];\n"
            "msg.action = 'method';\n"
            "return msg;"
        ),
        "outputs": 1,
        "x": 700,
        "y": 1560,
        "wires": [[cli_reject]],
    })

    for nid, label in [(cli_approve, "approve method"), (cli_reject, "reject method")]:
        nodes.append({
            "id": nid,
            "type": "OpcUa-Client",
            "z": TAB,
            "endpoint": OPCUA_ENDPOINT,
            "action": "method",
            "deadbandtype": "a",
            "deadbandvalue": 1,
            "time": 10,
            "timeUnit": "s",
            "certificate": "n",
            "localfile": "",
            "localkeyfile": "",
            "securitymode": "None",
            "securitypolicy": "None",
            "name": label,
            "x": 1000,
            "y": 1500 if "approve" in label else 1560,
            "wires": [[]],
        })

    json.dump(nodes, sys.stdout, indent=2, ensure_ascii=False)
    print()


if __name__ == "__main__":
    main()
